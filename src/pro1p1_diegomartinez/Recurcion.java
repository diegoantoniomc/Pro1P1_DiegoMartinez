    public static int[][] fill(int [][] array, int row,int col){
        if (row>=0 && row<array.length && col>=0 && col<array[row].length){
            int x = random.nextInt(10);
            array[row][col]=x;
        }//End if
        if (row<0 || row>= array.length || col<0 || col>=array[row].length){
            return array;
        }//End if 2
        else{
            if(col == array[row].length-1){
                return fill (array,row+1,0);
            }//End if 3
            else{
                return fill (array,row,col+1);
            }//End Else 2
        }//End Else 1
    }//End Fill
    
    public static String print (int [][] array, int row, int col, String res){
        if (row>=0 && row<array.length && col>=0 && col<array[row].length){
            res += "[" + array[row][col] + "]" + " ";
            if(array[row].length-1 == col){
                res+="\n";
            }//End if 2
        }//End if 1
        if (row<0 || row>= array.length || col<0 || col>=array[row].length){
            return res;
        }//End if 3
        else{
            if(col == array[row].length-1){
                return print(array,row+1,0,res);
            }//End if 4
            else{
                return print(array,row,col+1,res);
            }//End Else 2
        }//End Else 1
    }//End Print

