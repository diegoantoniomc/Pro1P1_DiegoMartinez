    private Object getCanListStr(ArrayList<Cancion> can) {
        String output = "";
        for (Cancion c : can) {
            output += Integer.toString(can.indexOf(c)+1)+". " + c.toString() + "\n";
        }
        return output;
    }
    
    
    
    
